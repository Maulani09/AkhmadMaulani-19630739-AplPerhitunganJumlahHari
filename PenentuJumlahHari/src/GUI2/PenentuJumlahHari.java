    private void buttomHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttomHitungActionPerformed
        if(textTahun.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Error: isian tahun kosong!","Error: isian tahun kosong!",JOptionPane.WARNING_MESSAGE);
        }else{
            int tahun = Integer.parseInt(textTahun.getText());
            int jumlahHari;
            if(jComboBox1.getSelectedItem().equals("Februari")){
                if ( ((tahun % 4 == 0) && ! (tahun % 100 == 0)) || (tahun % 400 == 0))
                    jumlahHari = 29;
                else jumlahHari = 28;
            }else if (jComboBox1.getSelectedItem().equals("April") || 
                    jComboBox1.getSelectedItem().equals("Juni") || 
                    jComboBox1.getSelectedItem().equals("September") ||
                    jComboBox1.getSelectedItem().equals("Nopember")){
                jumlahHari = 30;
            }else{
                jumlahHari = 31;
            }
            labelHasil.setText("Jumlah hari pada bulan " + 
                    jComboBox1.getSelectedItem() + 
                    " tahun " + tahun + " adalah " + jumlahHari);
        }
    }//GEN-LAST:event_buttomHitungActionPerformed

