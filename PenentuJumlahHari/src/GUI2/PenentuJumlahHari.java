    private void prosesSimpan() {
        try {
            BufferedWriter out = new BufferedWriter(new FileWriter("jumlahhari.txt"));
            out.write(labelHasil.getText());
            out.close();
        } catch (Exception e) {
            System.out.println("Error boss, check codingannya!");
        }
    }
    
